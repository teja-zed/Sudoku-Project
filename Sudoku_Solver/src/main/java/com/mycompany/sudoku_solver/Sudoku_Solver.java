    private void resetGame() {
        for (JButton[] rowBtns: sudokuBtns)
            for (JButton btn: rowBtns)
                if ( !predefSet.contains(btn) ) {
                    btn.setText("");
                    btn.setForeground( new JButton().getBackground() );
//                    btn.setBackground( new JButton().getBackground() );
//                    btn.setForeground(null);
                    btn.setBackground(null);
                    
                }
        
        // if game is won
        if ( victoryFlag ) {
            victoryFlag = false;
            greenCells = 0;
        }
        
        // reset globalSolutionFlag
        globalSolutionFlag = false;
        solutionBtn.setText("Solution");
        // reset globalCheckFlag
        globalCheckFlag = false;
        checkBtn.setText("Check Move");
        // clear userMap
        userMap.clear();
    }
    
    
    private void seeSolution() {
        // if game is won
        if ( victoryFlag ) {
            JFrame frame = new JFrame("Reset game");
            
            if (JOptionPane.showConfirmDialog(frame, "Do you want to reset the game?",
                    "Sudoku Puzzle", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
                resetGame();
            }
            return;
        }
        
        if ( !globalSolutionFlag ) {
            globalSolutionFlag = true;
            if ( globalCheckFlag ) {
                globalCheckFlag = false;
                checkBtn.setText("Check Move");
            }
            solutionBtn.setText("Hide Ans");
            
            Color correctColor = solutionBtn.getBackground();
            
            for (int i=0; i<sudokuBtns.length; i++)
                for (int j=0; j<sudokuBtns[i].length; j++)
                    if ( !predefSet.contains(sudokuBtns[i][j]) ) {
                        sudokuBtns[i][j].setText(solvedBoard[i][j]);
                        sudokuBtns[i][j].setForeground(Color.black);
                        sudokuBtns[i][j].setBackground(Color.white);
                        userMap.put(sudokuBtns[i][j], correctColor);
                    }
        }
        else {
            globalSolutionFlag = false;
            solutionBtn.setText("Solution");
            resetGame();
        }
    }
    
    
