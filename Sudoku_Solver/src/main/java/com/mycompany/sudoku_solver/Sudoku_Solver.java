    private void markCell(JButton btn, int i, int j) {
        // to help checkMoves()
        if ( globalCheckFlag )
            checkMoves();
        // if solution is seen
        if ( globalSolutionFlag )
            resetGame();
        // if game is won, don't do anything until game is manually reset
        if ( victoryFlag )
            return;
        
        // marking cell logic
        btn.setText(turn);
        btn.setBackground(Color.white);
        
        // to help checkMoves()
        int row = i-1;
        int col = j-1;
        Color correctColor = solutionBtn.getBackground();
        Color wrongColor = exitBtn.getBackground();
        Color checkColor = (btn.getText().equals(solvedBoard[row][col])) ?
                                                    correctColor : wrongColor;
        if (checkColor == correctColor) {
            if (userMap.get(btn) != correctColor)
                greenCells++;
        }
        else {
            if (userMap.get(btn) == correctColor)
                greenCells--;
        }
        
        userMap.put(btn, checkColor);
        
        // condition to win the game
        if (greenCells + predefSet.size() == 81)
            gameWon();
    }
    
    
    private void gameWon() {
        victoryFlag = true;
        solutionBtn.setText("Reset");
        checkBtn.setText("Reset");
        
        for (JButton btn: userMap.keySet())
            btn.setBackground(Color.orange);
        
        JOptionPane.showMessageDialog(this, "Congratulations. You have solved the Sudoku Puzzle",
                "Message", JOptionPane.INFORMATION_MESSAGE);
    }
    

